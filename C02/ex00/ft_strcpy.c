#include <unistd.h>

char	*ft_strcpy(char *dest, char *src)
{
	int	i;
	int	n;

	i = 0;
	while (i < n && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
